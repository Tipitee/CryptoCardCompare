#!/usr/bin/env node
/**
 * Audit blog_posts meta_description for DE and EN.
 * Flags: missing, too short (<80 chars), too long (>160 chars), or generic.
 *
 * Usage:
 *   source .env && node scripts/audit-blog-meta-descriptions.mjs
 *   source .env && node scripts/audit-blog-meta-descriptions.mjs --fix   (generates SQL)
 *
 * Output (--fix): scripts/fix-blog-meta-descriptions.sql
 */
import { createClient } from '@supabase/supabase-js';
import { writeFileSync } from 'fs';

const SUPABASE_URL = process.env.VITE_SUPABASE_URL;
const SUPABASE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY;
if (!SUPABASE_URL || !SUPABASE_KEY) { console.error('❌ env manquant'); process.exit(1); }
const sb = createClient(SUPABASE_URL, SUPABASE_KEY);

const FIX_MODE = process.argv.includes('--fix');
const LANGS = ['de', 'en'];
const MIN_LEN = 80;
const MAX_LEN = 160;

// Generic patterns that indicate auto-generated or placeholder meta descriptions
const GENERIC_PATTERNS = [
  /^découvrez/i,       // FR spill-over
  /^erfahren sie/i,    // German filler
  /^in diesem artikel/i,
  /^this article/i,
  /^learn about/i,
  /^find out/i,
  /^lire la suite/i,   // FR spill-over
];

function isGeneric(str) {
  return GENERIC_PATTERNS.some(p => p.test(str?.trim() ?? ''));
}

function classify(row) {
  const md = row.meta_description?.trim() ?? '';
  if (!md) return 'MISSING';
  if (md.length < MIN_LEN) return 'TOO_SHORT';
  if (md.length > MAX_LEN) return 'TOO_LONG';
  if (isGeneric(md)) return 'GENERIC';
  return 'OK';
}

// Truncate at last word boundary before maxLen
function truncateAtWord(str, maxLen = MAX_LEN) {
  if (str.length <= maxLen) return str;
  const cut = str.slice(0, maxLen - 3);
  const lastSpace = cut.lastIndexOf(' ');
  return (lastSpace > maxLen * 0.6 ? cut.slice(0, lastSpace) : cut) + '...';
}

// Generate a simple meta description from title + excerpt
function generateMeta(row) {
  const title = (row.title ?? '').trim();
  const excerpt = (row.excerpt ?? '').trim();
  const existing = (row.meta_description ?? '').trim();
  // For TOO_LONG: just truncate the existing meta description at word boundary
  if (existing.length > MAX_LEN) return truncateAtWord(existing);
  // Use excerpt if decent length
  if (excerpt.length >= MIN_LEN && excerpt.length <= MAX_LEN) return excerpt;
  if (excerpt.length > MAX_LEN) return truncateAtWord(excerpt);
  // Combine title + excerpt
  const combined = excerpt ? `${title} — ${excerpt}` : title;
  return truncateAtWord(combined);
}

console.log(`\n🔍 Auditing blog meta_descriptions for langs: ${LANGS.join(', ')}\n`);

let { data: posts, error } = await sb
  .from('blog_posts')
  .select('id, lang, slug, title, excerpt, meta_description')
  .in('lang', LANGS)
  .order('lang')
  .order('slug');

if (error) { console.error('❌ Supabase error:', error.message); process.exit(1); }

const issues = posts.filter(p => classify(p) !== 'OK');
const byStatus = {};
for (const p of posts) {
  const s = classify(p);
  byStatus[s] = (byStatus[s] ?? 0) + 1;
}

console.log('📊 Summary:');
console.log(`  Total DE+EN posts: ${posts.length}`);
for (const [k, v] of Object.entries(byStatus)) {
  console.log(`  ${k}: ${v}`);
}

console.log(`\n⚠️  Issues (${issues.length}):`);
for (const p of issues) {
  const status = classify(p);
  const md = p.meta_description?.trim() ?? '';
  console.log(`  [${p.lang}] ${p.slug} → ${status} (${md.length} chars)`);
  if (md) console.log(`    "${md.slice(0, 80)}${md.length > 80 ? '...' : ''}"`);
}

if (FIX_MODE) {
  const sqlLines = [
    '-- Auto-generated by audit-blog-meta-descriptions.mjs',
    '-- Review before running: only fixes MISSING / TOO_SHORT / GENERIC posts',
    '',
  ];
  let fixCount = 0;
  for (const p of issues) {
    const status = classify(p);
    const newMeta = generateMeta(p).replace(/'/g, "''");
    sqlLines.push(`UPDATE blog_posts SET meta_description = '${newMeta}' WHERE id = '${p.id}'; -- [${p.lang}] ${p.slug}`);
    fixCount++;
  }
  const outPath = 'scripts/fix-blog-meta-descriptions.sql';
  writeFileSync(outPath, sqlLines.join('\n') + '\n');
  console.log(`\n✅ SQL written to ${outPath} (${fixCount} fixes)`);
}
